     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($request)
    {
        $this->request = $request;
    }

    /**
     * Build the message.d
     *
     * @return $this
     */
    public function build()
    {
        return $this->view('kritik.kritikMail');

    }
}
